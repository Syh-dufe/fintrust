import pandas as pd
import numpy as np

# ==========================================================
# 1. 多源替代数据采集
# ==========================================================

# --------------------------------
# 交易行为数据
# --------------------------------

transaction_data = {
    "月均交易额": 92,
    "订单稳定性": 85,
    "供应链履约": 88,
    "回款周期": 80
}

# --------------------------------
# 生态与环保数据
# --------------------------------

environment_data = {
    "环保合规": 91,
    "碳汇贡献": 87,
    "生态修复": 82,
    "尾水处理": 90
}

# --------------------------------
# 物联网感知数据（IoT）
# --------------------------------

iot_data = {
    "水温稳定性": 84,
    "溶氧稳定性": 89,
    "PH值波动": 78,
    "摄食行为健康度": 92
}

# --------------------------------
# 外部动态数据
# --------------------------------

external_data = {
    "气象风险": 70,
    "市场价格波动": 75,
    "政策匹配度": 88,
    "舆情评价": 86
}

# ==========================================================
# 2. 数据标准化处理
# ==========================================================

def normalize(data_dict):
    normalized = {}
    for k, v in data_dict.items():
        normalized[k] = min(max(v, 0), 100)
    return normalized

transaction_data = normalize(transaction_data)
environment_data = normalize(environment_data)
iot_data = normalize(iot_data)
external_data = normalize(external_data)

# ==========================================================
# 3. 多源数据融合
# ==========================================================

all_data = {
    **transaction_data,
    **environment_data,
    **iot_data,
    **external_data
}

# ==========================================================
# 4. 替代数据权重体系
# ==========================================================

weights = {
    "月均交易额": 0.10,
    "订单稳定性": 0.06,
    "供应链履约": 0.07,
    "回款周期": 0.05,

    "环保合规": 0.10,
    "碳汇贡献": 0.08,
    "生态修复": 0.07,
    "尾水处理": 0.06,

    "水温稳定性": 0.05,
    "溶氧稳定性": 0.05,
    "PH值波动": 0.03,
    "摄食行为健康度": 0.08,

    "气象风险": 0.04,
    "市场价格波动": 0.04,
    "政策匹配度": 0.06,
    "舆情评价": 0.06
}

# ==========================================================
# 5. 综合信用评分模型
# ==========================================================

credit_score = 0

for key in all_data:
    credit_score += all_data[key] * weights[key]

credit_score = round(credit_score, 2)

# ==========================================================
# 6. 风险识别逻辑
# ==========================================================

risk_warning = []

if external_data["气象风险"] < 75:
    risk_warning.append("存在气象灾害风险")

if environment_data["环保合规"] < 80:
    risk_warning.append("环保风险偏高")

if transaction_data["回款周期"] < 70:
    risk_warning.append("资金回笼能力较弱")

if iot_data["溶氧稳定性"] < 75:
    risk_warning.append("养殖环境稳定性不足")

# ==========================================================
# 7. 信用等级划分
# ==========================================================

if credit_score >= 90:
    credit_level = "AAA"
    risk_level = "低风险"

elif credit_score >= 80:
    credit_level = "AA"
    risk_level = "较低风险"

elif credit_score >= 70:
    credit_level = "A"
    risk_level = "中等风险"

else:
    credit_level = "B"
    risk_level = "高风险"

# ==========================================================
# 8. 绿色信用分计算
# ==========================================================

green_credit_score = (
    environment_data["环保合规"] * 0.4 +
    environment_data["碳汇贡献"] * 0.3 +
    environment_data["生态修复"] * 0.2 +
    environment_data["尾水处理"] * 0.1
)

green_credit_score = round(green_credit_score, 2)

# ==========================================================
# 9. 授信额度动态计算
# ==========================================================

base_credit = 100000

credit_limit = (
    base_credit *
    (credit_score / 100) *
    (green_credit_score / 100 + 0.5)
)

credit_limit = int(credit_limit)

# ==========================================================
# 10. AI增信建议生成
# ==========================================================

suggestions = []

if green_credit_score < 85:
    suggestions.append("建议加强生态修复与尾水治理")

if transaction_data["订单稳定性"] < 80:
    suggestions.append("建议提升订单稳定性与长期合作比例")

if iot_data["PH值波动"] < 80:
    suggestions.append("建议优化养殖水质监测系统")

if external_data["舆情评价"] < 80:
    suggestions.append("建议加强品牌与市场信誉建设")

# ==========================================================
# 11. 输出信用评估结果
# ==========================================================

result = {
    "综合信用分": credit_score,
    "绿色信用分": green_credit_score,
    "信用等级": credit_level,
    "风险等级": risk_level,
    "可授信额度": f"{credit_limit} 元",
    "风险预警": risk_warning,
    "增信建议": suggestions
}

# ==========================================================
# 12. 转换为结果表
# ==========================================================

result_df = pd.DataFrame({
    "指标": list(result.keys()),
    "结果": list(result.values())
})

print("======================================")
print(" FinTrust 替代数据驱动信用评估结果 ")
print("======================================")

print(result_df)

